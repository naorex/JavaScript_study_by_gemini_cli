// 問題1: `let` を使って `a` という名前の変数を宣言し、数値 `10` を代入してください。
let a = 10;

// 問題2: `const` を使って `b` という名前の定数を宣言し、数値 `20` を代入してください。
const b = 20;

// 問題3: `a` と `b` を足した結果を、`sum` という名前の新しい変数に代入してください。
let sum = a + b;

// 問題4: `a` から `b` を引いた結果を、`difference` という名前の新しい変数に代入してください。
let difference = a - b;

// 問題5: `a` と `b` を掛けた結果を、`product` という名前の新しい変数に代入してください。
let product = a * b;

// 問題6: `a` を `b` で割った結果を、`quotient` という名前の新しい変数に代入してください。
let quotient = a / b;

// 生成した変数をエクスポートします
export { a, b, sum, difference, product, quotient };
